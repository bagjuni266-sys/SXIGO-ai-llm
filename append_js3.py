J('')
J('function formatMessage(text) {')
J('  let html = text')
J('    .replace(/&/g, "&amp;")')
J('    .replace(/</g, "&lt;")')
J('    .replace(/>/g, "&gt;")')
J('    .replace(/```(\\w*)\\n?([\\s\\S]*?)```/g, function(_, lang, code) {')
J('      const enc = encodeURIComponent(code);')
J('      return "<pre><button class=\\"copy-btn\\" onclick=\\"copyCode(this)\\" data-code=\\"" + enc + "\\">Copy</button><code>" + code + "</code></pre>";')
J('    })')
J('    .replace(/`([^`]+)`/g, "<code>$1</code>")')
J('    .replace(/\\*\\*(.+?)\\*\\*/g, "<strong>$1</strong>")')
J('    .replace(/\\*(.+?)\\*/g, "<em>$1</em>")')
J('    .replace(/\\n\\n/g, "</p><p>")')
J('    .replace(/\\n/g, "<br>");')
J('  return "<p>" + html + "</p>";')
J('}')

J('')
J('function copyMessage(idx) {')
J('  const text = messages[idx] ? messages[idx].content : "";')
J('  navigator.clipboard.writeText(text).then(() => showToast("Copied!", "success"));')
J('}')

J('')
J('function editMessage(idx) {')
J('  const msg = messages[idx];')
J('  if (!msg) return;')
J('  const newText = prompt("Edit message:", msg.content);')
J('  if (newText !== null) {')
J('    msg.content = newText;')
J('    msg.edited = true;')
J('    saveCurrentConversation();')
J('    renderMessages();')
