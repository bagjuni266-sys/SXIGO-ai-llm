J('    const avatar = document.createElement("div");')
J('    avatar.className = "avatar " + msg.role;')
J('    avatar.textContent = msg.role === "user" ? "U" : "AI";')
J('    const content = document.createElement("div");')
J('    content.className = "message-content";')
