J('  const ws = document.getElementById("welcomeScreen");')
J('  if (ws && ws.parentNode) ws.remove();')
J('  messages.forEach((msg, idx) => {')
J('    const wrapper = document.createElement("div");')
J('    wrapper.className = "message-wrapper " + msg.role;')
